// Arabic normalization: drop diacritics/tatweel, unify alef/ya/ta-marbuta/hamza
// so "إجازة" / "اجازه" / "أجازة" all match the same intent.
function norm(s: string): string {
  return (s || "")
    .toLowerCase()
    .replace(/[ً-ْٰـ]/g, "")
    .replace(/[إأآ]/g, "ا")
    .replace(/ى/g, "ي")
    .replace(/ة/g, "ه")
    .replace(/ؤ/g, "و")
    .replace(/ئ/g, "ي")
    .replace(/\s+/g, " ")
    .trim();
}

