const BACK_TO_MODULES_CHIP = "📚 كل وحدات الكورس";

function courseOverviewReply(): GuideReply {
  const list = HR_COURSE.map(
    (m, i) => `${m.icon} ${i + 1}. ${m.title}`,
  ).join("\n");
  return {
    text:
      `🎓 منهج الموارد البشرية الاحترافي — من نِظام\n` +
      `أنا اتدرّبت على منهج HR متكامل: ${HR_COURSE_MODULE_COUNT} وحدات و${HR_COURSE_LESSON_COUNT} درس. اسألني في أي موضوع، أو اختار وحدة تبدأ بيها 👇\n\n${list}`,
    chips: HR_COURSE.map((m) => m.title),
  };
}

function moduleReply(m: CourseModule): GuideReply {
  const list = m.lessons.map((l, i) => `${i + 1}. ${l.t}`).join("\n");
  return {
    text: `${m.icon} وحدة: ${m.title}\n\nدروس الوحدة:\n${list}\n\nاختار درس تقراه 👇`,
    chips: [...m.lessons.map((l) => l.t), BACK_TO_MODULES_CHIP],
  };
}

function lessonReply(m: CourseModule, idx: number): GuideReply {
  const l = m.lessons[idx];
  const next = m.lessons[idx + 1];
  const chips: string[] = [];
  if (next) chips.push(next.t);
  chips.push(BACK_TO_MODULES_CHIP);
  return { text: `${m.icon} ${l.t}\n\n${l.body}`, chips, goto: l.goto };
}

// Course navigation: explicit chip clicks (exact module/lesson title) + the
// "back to modules" chip. Exact-match so it never hijacks a free-text question.
function matchCourse(q: string): GuideReply | null {
  if (q.includes(norm("كل وحدات الكورس")) || q === norm("كل الوحدات"))
    return courseOverviewReply();
  for (const m of HR_COURSE) {
    if (q === norm(m.title)) return moduleReply(m);
    for (let i = 0; i < m.lessons.length; i += 1) {
      if (q === norm(m.lessons[i].t)) return lessonReply(m, i);
    }
  }
  return null;
}

function taskReply(t: GuideTask): GuideReply {
  const steps = t.steps.map((s, i) => `${i + 1}. ${s}`).join("\n");
  const note = t.note ? `\n\n💡 ${t.note}` : "";
  return { text: `🛠️ ${t.title}\n\n${steps}${note}`, chips: TOPIC_CHIPS.slice(0, 3), goto: t.goto };
}

function scoreKeys(q: string, keys: string[]): number {
  let s = 0;
  for (const k of keys) {
    const nk = norm(k);
    if (nk && q.includes(nk)) s += nk.length; // longer match = stronger signal
  }
  return s;
}

export function respond(query: string): GuideReply {
  const q = norm(query);
  if (!q) return { text: "اكتبلي سؤالك وأنا أساعدك 🙂", chips: TOPIC_CHIPS };

  // Explicit course navigation (chip clicks: exact module/lesson title, or the
  // "back to modules" chip) wins immediately — it's an unambiguous click.
  const courseNav = matchCourse(q);
  if (courseNav) return courseNav;

  // Score the layers, then prefer the most specific.
  let bestTask: GuideTask | null = null;
  let taskScore = 0;
  for (const t of TASKS) {
    const sc = scoreKeys(q, t.keys);
    if (sc > taskScore) {
      taskScore = sc;
      bestTask = t;
    }
  }

  let bestSkill: GuideSkill | null = null;
  let skillScore = 0;
  for (const sk of SKILLS) {
    const sc = scoreKeys(q, sk.keys);
    if (sc > skillScore) {
      skillScore = sc;
      bestSkill = sk;
    }
  }

  let bestSpecial: Intent | null = null;
  let specialScore = 0;
  let bestPage: Intent | null = null;
  let pageScore = 0;
  for (const it of INTENTS) {
    const sc = scoreKeys(q, it.keys);
    if (it.special) {
      if (sc > specialScore) {
        specialScore = sc;
        bestSpecial = it;
      }
    } else if (it.page) {
      if (sc > pageScore) {
        pageScore = sc;
        bestPage = it;
      }
    }
  }

  // Course OVERVIEW by keyword ("الكورس", "المنهج", "علمني موارد بشرية"…).
  const courseScore = scoreKeys(q, COURSE_KEYS);

  const directScore = Math.max(taskScore, skillScore, courseScore);

  // An explicit tour request always wins over weaker matches.
  if (bestSpecial?.special === "tour" && specialScore >= directScore)
    return { text: "يلا نبدأ جولة سريعة في النظام! 🧭", chips: [], tour: true };

  // Most specific actionable answer wins — the HR course overview, a
  // professional HR/labor-law skill, or a task how-to — when it matches at
  // least as strongly as special/page.
  if (directScore > 0 && directScore >= pageScore && directScore >= specialScore) {
    if (courseScore === directScore) return courseOverviewReply();
    if (bestSkill && skillScore >= taskScore) return skillReply(bestSkill);
    if (bestTask) return taskReply(bestTask);
  }

  if (bestSpecial && specialScore > 0 && specialScore >= pageScore) {
    if (bestSpecial.special === "greet")
      return { text: "أهلاً بيك! 👋 أنا نِظّوم، مرشدك في النظام. قوللي عايز تعمل إيه (زي «عايز أربط البصمة») أو خد جولة سريعة.", chips: TOPIC_CHIPS };
    if (bestSpecial.special === "thanks")
      return { text: "العفو 😊 أي وقت تحتاجني أنا هنا في الركن.", chips: TOPIC_CHIPS };
    if (bestSpecial.special === "who")
      return {
        text:
          "أنا نِظّوم 🤖 — مرشدك ومساعدك في الموارد البشرية جوّه نِظام. بقدر:\n" +
          "• أوريك خطوات أي مهمة («عايز أعمل كذا») وأمشي معاك صفحة بصفحة.\n" +
          "• أجاوبك في قانون العمل المصري: إجازات، أوفرتايم، تأمينات، فترة اختبار، إنذار، نهاية خدمة…\n" +
          "• أديك تشيك-ليست جاهزة (روتين المرتبات، تعيين موظف، إنهاء خدمة) وأحلّ مشاكل شائعة.\n" +
          "• وكمان عندي 🎓 منهج موارد بشرية احترافي كامل تقدر تتعلّم منه — اكتب «الكورس».\n" +
          "كله جوّاك من غير إنترنت ولا توقّف. اسألني عن أي حاجة 🙂",
        chips: TOPIC_CHIPS,
      };
    if (bestSpecial.special === "tour")
      return { text: "يلا نبدأ جولة سريعة في النظام! 🧭", chips: [], tour: true };
  }

  if (bestPage?.page && pageScore > 0) {
    const page = GUIDE_PAGES.find((p) => p.match === bestPage!.page);
    if (page)
      return { text: pageReplyText(page), chips: ["افتح الصفحة دي ←", ...TOPIC_CHIPS.slice(0, 2)], goto: page.match };
  }

  return {
    text: "مش متأكد فهمت قصدك بالظبط 🤔 قوللي اللي عايز تعمله (مثلاً: «عايز أربط البصمة» أو «عايز أعمل مرتبات») وأديك الخطوات، أو اختار من تحت 👇",
    chips: TOPIC_CHIPS,
  };
}
