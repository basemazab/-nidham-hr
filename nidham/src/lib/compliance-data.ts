// ============================================================================
// Compliance Knowledge Base — Egyptian inspection authorities
// ============================================================================
//
// Source of truth for the /dashboard/compliance guide. Every authority
// here is a real Egyptian regulator that can show up at the company's
// door, demand papers, and fine the owner for missing items. Content
// is grounded in published Egyptian law:
//
//   - قانون العمل رقم 12 لسنة 2003 + اللائحة التنفيذية + قانون 121/2008
//     (مراجعات قانون العمل)
//   - قانون التأمينات الاجتماعية رقم 148 لسنة 2019
//   - قانون الضريبة على الدخل (المعدّل بـ 175/2023) + قانون 67/2016 لضريبة
//     القيمة المضافة
//   - قرار وزير القوى العاملة رقم 211 لسنة 2003 (السلامة والصحة المهنية)
//   - قانون البيئة رقم 4 لسنة 1994 + قانون 105/2015
//   - قانون 168 لسنة 2023 (سياسة منع التحرش — يلزم +50 موظف)
//
// Nothing here is legal advice; it's an HR-grade checklist. Owners
// should still consult a labor lawyer for edge cases.

export type ComplianceItem = {
  /** Short Arabic name for the document/procedure */
  title: string;
  /** Two-three line Arabic description: what it is + when it's required */
  description: string;
  /** Optional: link to the form/template in /dashboard/forms */
  formLink?: string;
  /** Optional: legal article reference (e.g. "Article 87") */
  legalRef?: string;
  /** When this item is needed (always / hire / annual / on-event) */
  cadence?:
    | "always"
    | "on-hire"
    | "on-termination"
    | "monthly"
    | "annual"
    | "on-incident"
    | "on-event";
  /** Companies size threshold (e.g. "> 50 موظف") */
  threshold?: string;
};

export type ComplianceSection = {
  title: string;
  items: ComplianceItem[];
};

export type ComplianceAuthority = {
  /** URL slug — used in /dashboard/compliance/[slug] */
  slug: string;
  /** Full Arabic name */
  name: string;
  /** Short tagline shown on the hub card */
  tagline: string;
  /** Emoji used on the hub card */
  icon: string;
  /** Color theme — drives the hub card + page header */
  color: "cyan" | "amber" | "rose" | "emerald" | "violet" | "slate" | "sky";
  /** Which ministry it falls under */
  ministry: string;
  /** Legal basis (laws + their numbers) */
  legalBasis: string[];
  /** How often inspections typically happen */
  inspectionFrequency: string;
  /** A 1-2 sentence overview of what this authority looks for */
  overview: string;
  /** Document checklist, grouped into sections */
  sections: ComplianceSection[];
  /** Common violations + the typical penalty */
  violations: { violation: string; penalty: string }[];
  /** Practical preparation tips */
  tips: string[];
};

