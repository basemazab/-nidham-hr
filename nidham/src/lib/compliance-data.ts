export const COMPLIANCE_AUTHORITIES: ComplianceAuthority[] = [
  LABOR_OFFICE,
  SOCIAL_INSURANCE,
  OCCUPATIONAL_SAFETY,
  TAX_AUTHORITY,
  CIVIL_DEFENSE,
  INDUSTRIAL_SECURITY,
  ENVIRONMENT,
];

export function getAuthorityBySlug(slug: string): ComplianceAuthority | null {
  return COMPLIANCE_AUTHORITIES.find((a) => a.slug === slug) ?? null;
}

// ----------------------------------------------------------------------------
// Item count + totals (for the hub progress bars)
// ----------------------------------------------------------------------------
export function countItems(a: ComplianceAuthority): number {
  return a.sections.reduce((sum, s) => sum + s.items.length, 0);
}

export function totalItems(): number {
  return COMPLIANCE_AUTHORITIES.reduce((sum, a) => sum + countItems(a), 0);
}
